public class Condicional_For_Ejercicio {
    public static void main(String[] args) {

        //   ( Creación de la variable; Creación de la condición del ciclo; Creación del incremento del ciclo)
        for (int numeroFor = 0; numeroFor <= 3; numeroFor++) {
            // Impresión en pantalla del ciclo.
            System.out.println("El númeroFor es: " +numeroFor);
        }
    }
}