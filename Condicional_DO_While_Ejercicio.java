public class Condicional_DO_While_Ejercicio {

        public static void main(String[] args) {
            // Creación de la variable.
            int numerodowild = -9;
            // Ejecución inicial de la operación del incremento.
            do {
                numerodowild = numerodowild+1;
                // Validación de la condición para detener el ciclo.
            } while (numerodowild<3);
            // impresión en consola del valor final del ciclo.
            System.out.println("el numero do while es: " + numerodowild);


            }
        }



